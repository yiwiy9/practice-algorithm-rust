use proconio::input;
use std::collections::BinaryHeap;

/// https://atcoder.jp/contests/abc407/tasks/abc407_e
/// - 何ごとの話にすると見やすい？ 何だけ持てばいい？
///   `(` にする位置を前から選ぶ話にして、各奇数長 prefix までに選べる候補集合だけ持つ。
/// - それについて、何が分かれば答えになる？
///   `k` 個目の `(` を締切 `2k-1` までの候補から選べれば、選んだ位置の `A_i` の合計が答えになる。
/// - 何を捨ててよく、なぜそれで足りる？ 何が効く / 何が禁止？
///   正しい括弧列の条件は「長さ `2k-1` までに `(` が `k` 個以上」で足りるので、具体的な括弧列 DP は不要。
///   その時点で候補集合から最大を選んでも、後で選ぶ小さい候補と交換でき、以後の prefix 条件を壊さない。
/// - その情報をどう更新 / 判定 / 集計すれば実装できる？
///   先頭は必ず `(`、末尾は必ず `)` とし、各 step で候補を 2 個追加して max-heap から 1 個取り出す。
///
/// 解説AC
/// 思いつき方:
/// `dp[j] = ここまで見て "(" を j 個選んだ最大値` は自然だが、必要なのは各 prefix での個数下限だけ。
/// 偶数長ではなく奇数長 `2k-1` で必要な `(` の個数が増えるので、候補が 2 個増えるたびに 1 個選ぶ締切付き貪欲に言い換える。
fn main() {
    input! {
        t: usize,
    }

    for _ in 0..t {
        input! {
            n: usize,
            a: [usize; 2*n],
        }

        let mut heap = BinaryHeap::new();
        let mut ans = a[0];

        for k in 1..n {
            // 長さ 2k+1 の prefix までに、追加で 1 個 `(` を選ぶ必要がある。
            heap.push(a[2 * k - 1]);
            heap.push(a[2 * k]);
            ans += heap.pop().unwrap();
        }

        println!("{}", ans);
    }
}
