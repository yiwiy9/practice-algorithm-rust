use proconio::{input, marker::Chars};

/// # ABC452 D 復習用メモ付き AC コード
///
/// https://atcoder.jp/contests/abc452/tasks/abc452_d
///
/// ## 問題の本質
/// 各部分文字列 `S[l..=r]` について、`T` を**部分列**として含まないものの個数を数える。
///
/// 総部分文字列数は `n * (n + 1) / 2` なので、
///
/// - `T` を部分列として**含む**部分文字列数
///
/// を数えて引けばよい。
///
/// - 固定するもの: 左端 `l`
/// - 欲しい量: 各 `l` に対する最小成立右端 `r_min`
/// - 必要クエリ: `pos` 以降で文字 `c` が最初に出る位置
/// - 単調性 / 境界: `r` を伸ばすほど条件は満たしやすい
///
/// ---
///
/// ## 今回の敗因
///
/// ### 1. 観察はできていたのに、既存パターンに無理やり落とそうとした
/// 途中で
///
/// - 左端を固定すると、成立する右端は境界になる
/// - 各左端について最小の成立右端が分かれば数えられそう
/// - 判定自体は `T` を順に追えばできそう
///
/// というかなり本質的な観察はできていた。
///
/// しかしその後で
///
/// - 「これを DP にするには？」
/// - 「端を固定するなら尺取りでは？」
///
/// という**アルゴリズム名ベースの思考**に引っ張られてしまい、
/// 観察を素直に伸ばせなかった。
///
/// ### 2. DP が「欲しい量」を直接持っていなかった
/// 典型的な部分列 DP は
///
/// - `S` 全体から `T` を何通り作れるか
///
/// を数えやすいが、この問題で欲しいのは
///
/// - 各区間 `[l, r]` が成立するか
/// - 左端ごとの最小成立右端はどこか
///
/// である。
///
/// つまり「通り数」ではなく「境界位置」が欲しい。
/// DP を書いても、数えたい対象とズレやすい。
///
/// ### 3. 「端を固定したら尺取り」という固定観念
/// 左端固定で考えるのは正しいが、
/// それをそのまま尺取りに結びつけたのが詰まりの原因。
///
/// 尺取りで本当に大事なのは
///
/// - 右端を伸ばしたときの更新が簡単か
/// - 左端を縮めたときの更新が簡単か
///
/// であり、今回は
///
/// - 左端を 1 つ進めたとき
/// - それまで使っていた部分列マッチをきれいに更新しづらい
///
/// ので、尺取りにうまく乗らない。
///
/// ### 4. `next_pos` が出てこなかった
/// 判定自体は
///
/// - 位置 `pos` から次に必要な文字を探す
///
/// という形で見えていたのに、
/// その「欲しい操作」を前計算すればよい、まで辿れなかった。
///
/// これは
///
/// - まず愚直な判定を言語化する
/// - その判定に必要なクエリを言語化する
/// - そのクエリを高速化する前計算を考える
///
/// という順番で考えると出やすくなる。
///
/// ---
///
/// ## 本番で勝つための理想の思考ログ
///
/// ### Step 1. まず区間ごとの判定問題として見る
/// `S[l..=r]` が `T` を部分列として含むか？
///
/// ### Step 2. 左端を固定したときの単調性を見る
/// 左端 `l` を固定すると、`r` を右に伸ばすほど条件は満たしやすくなる。
/// 一度 `T` を含んだら、それ以降もずっと含む。
///
/// → 各 `l` について
///
/// - 「初めて成立する最小右端」
///
/// が分かれば数えられる。
///
/// ### Step 3. その最小右端をどう求めるか考える
/// `T` を左から順に、できるだけ早くマッチさせればよい。
///
/// つまり必要なのは
///
/// - 「位置 `pos` 以降で文字 `c` が最初に現れる位置」
///
/// というクエリ。
///
/// ### Step 4. 欲しいクエリを前計算する
/// それが `next_pos[pos][c]`。
///
/// - `next_pos[i][c] = i 以上で文字 c が最初に出る位置`
/// - なければ `n`
///
/// を後ろから前計算する。
///
/// ### Step 5. 各左端から `T` を順にジャンプする
/// `l` ごとに
///
/// - `pos = l`
/// - `T` の各文字 `ch` について
///   - `next_pos[pos][ch]` に飛ぶ
///   - 見つからなければこの `l` では不成立
///
/// 最後に使った位置が最小右端。
///
/// ### Step 6. まとめて数える
/// 最小右端を `r_min` とすると
///
/// - `r >= r_min` の区間は全部成立
///
/// なので、その個数を足す。
///
/// ---
///
/// ## 詰まったときに立ち返るチェックポイント
///
/// 1. 今数えたいのは「区間」か？「選び方の数」か？
/// 2. 欲しいのは「通り数」か？「成立境界」か？
/// 3. 左端固定 / 右端固定で単調性はあるか？
/// 4. 何が分かれば一括で数えられるか？
/// 5. まず愚直判定は書けるか？
/// 6. その愚直判定で欲しいクエリは何か？
/// 7. そのクエリは前計算できないか？
/// 8. 「これを DP / 尺取りにするには？」ではなく、
///    「この観察から何が分かれば答えになるか？」に戻れているか？
///
/// ---
///
/// ## 実装方針
///
/// `next_pos[i][c]` を前計算して、各左端 `l` から
/// `T` を順に最短でマッチさせる。
///
/// 計算量:
///
/// - `next_pos` 構築: `O(26N)`
/// - 各 `l` について `T` をなめる: `O(NM)`
///
/// 制約 `|S| <= 2e5`, `|T| <= 50` なら十分間に合う。
fn main() {
    input! {
        s: Chars,
        t: Chars,
    }

    let n = s.len();

    // next_pos[i][c]:
    //   S の i 文字目以降で、文字 c が最初に現れる位置
    //   存在しなければ n
    //
    // これを持つと、「今いる位置以降で次に必要な文字はどこか？」が O(1) で分かる。
    let mut next_pos = vec![[n; 26]; n + 1];

    // 後ろから埋める。
    // next_pos[i] は基本的に next_pos[i+1] を引き継ぎ、
    // S[i] だけを上書きすればよい。
    for i in (0..n).rev() {
        next_pos[i] = next_pos[i + 1];
        let c = (s[i] as u8 - b'a') as usize;
        next_pos[i][c] = i;
    }

    // T を部分列として含む部分文字列数
    let mut contain = 0usize;

    // 左端 l を固定して考える。
    for l in 0..n {
        // pos:
        //   次の文字を探し始める位置
        let mut pos = l;
        let mut ok = true;

        // T を左から順に、最短でマッチさせる。
        // これで最後に使った位置が「最小成立右端」になる。
        for &ch in &t {
            let c = (ch as u8 - b'a') as usize;
            let nxt = next_pos[pos][c];

            if nxt == n {
                // この左端 l からは T を最後まで作れない
                ok = false;
                break;
            }

            // 次は nxt の次の位置から探す
            pos = nxt + 1;
        }

        if ok {
            // 最後に使った位置は pos - 1
            // よって最小成立右端 r_min = pos - 1
            //
            // この l に対して成立する区間は
            //   [l..r_min], [l..r_min+1], ..., [l..n-1]
            // の合計 n - r_min 個
            //
            // = n - (pos - 1)
            // = n - pos + 1
            contain += n - pos + 1;
        }
    }

    let total = n * (n + 1) / 2;
    let ans = total - contain;

    println!("{}", ans);
}
